# Definiendo una variable
nombre="Angel"

# Concatenar con +
bienvenida= "Hola "+ nombre + " ¿Como estas 1?"

# Concatenar con f-string
bienvenida= f"Hola {nombre} ¿Como estas 2?"

# borrar datos de la memoria
del nombre

# operador de pertenencia (in / not in)
print ("Hola" in bienvenida)       #True
print ("Hola" not in bienvenida)   #False